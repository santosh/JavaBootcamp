package dev.santoshk;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 6;
        int b = 4;

        int t1 = a + b;
        int t2 = a - b;
        int t3 = a * b;
        int t4 = a / b;

        System.out.println("Addition is " + t1);
        System.out.println("Subtraction is " + t2);
        System.out.println("Multiplication is " + t3);
        System.out.println("Division is " + t4);
    }
}
