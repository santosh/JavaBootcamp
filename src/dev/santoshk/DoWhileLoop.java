package dev.santoshk;

public class DoWhileLoop {
    public static void main(String[] args) {
        int i = 5;
        do {
            System.out.println(i);
            i++;
        } while (i < 10);

        int j = 5;
        while (j < 10) {
            System.out.println(j);
            j++;
        }
    }
}
